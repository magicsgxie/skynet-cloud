package cn.uway.skynet.cloud.upms.service;


import com.baomidou.mybatisplus.extension.service.IService;
import cn.uway.skynet.cloud.upms.entity.SysMenu;
import cn.uway.skynet.cloud.upms.vo.MenuVO;
import cn.uway.skynet.cloud.common.core.util.R;

import java.util.List;

/**
 * <p>
 * 菜单权限表 服务类
 * </p>
 *
 * @author magic.s.g.xie
 * @since 2019/2/1
 */
public interface SysMenuService extends IService<SysMenu> {
	/**
	 * 通过角色编号查询URL 权限
	 *
	 * @param roleId 角色ID
	 * @return 菜单列表
	 */
	List<MenuVO> findMenuByRoleId(Long roleId);

	/**
	 * 级联删除菜单
	 *
	 * @param id 菜单ID
	 * @return 成功、失败
	 */
	R removeMenuById(Integer id);

	/**
	 * 更新菜单信息
	 *
	 * @param sysMenu 菜单信息
	 * @return 成功、失败
	 */
	Boolean updateMenuById(SysMenu sysMenu);
}
