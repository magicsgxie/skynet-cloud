
namespace UWay.Skynet.Cloud.Data.Common
{
    public enum DateParts
    {
        /// <summary>
        /// 年份
        /// </summary>
        Year,
        /// <summary>
        /// 季度
        /// </summary>
        Quarter,
        /// <summary>
        /// 月份
        /// </summary>
        Month,
        /// <summary>
        /// 表示的日期为该月中的第几天
        /// </summary>
        Day,
        /// <summary>
        /// 小时
        /// </summary>
        Hour,
        /// <summary>
        /// 分钟
        /// </summary>
        Minute,
        /// <summary>
        /// 秒
        /// </summary>
        Second,


        /// <summary>
        /// 毫秒
        /// </summary>
        Millisecond,
        /// <summary>
        /// 微秒
        /// </summary>
        Microsecond,
        /// <summary>
        /// 纳秒
        /// </summary>
        Nanosecond,
        /// <summary>
        /// 表示的日期是该年中的第几周
        /// </summary>
        Week,
        /// <summary>
        /// 日期
        /// </summary>
        Date,
        /// <summary>
        /// 时间
        /// </summary>
        Time,
        /// <summary>
        /// 表示的日期是该年中的第几天
        /// </summary>
        DayOfYear,
        /// <summary>
        /// 表示的日期是星期几
        /// </summary>
        DayOfWeek,
        /// <summary>
        /// 仅作为框架内部使用
        /// </summary>
        TimeSpan,
        /// <summary>
        /// 工作日
        /// </summary>
        WorkDay,
    }
}
